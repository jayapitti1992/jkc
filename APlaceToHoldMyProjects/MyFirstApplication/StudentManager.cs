using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace MyFirstApplication
{
    class StudentManager
    {
        public int EldestStudent(List<Student> students)
        {
            int age = 0;
            foreach (Student student in students)
            {
                if (student.Age > age)
                {
                    age = student.Age;
                }
            }
            return age;
        }
    }
}
